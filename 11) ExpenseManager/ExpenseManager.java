import java.io.*; // –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –≤—Å–µ –∫–ª–∞—Å—Å—ã –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –≤–≤–æ–¥–æ–º/–≤—ã–≤–æ–¥–æ–º (—Ñ–∞–π–ª–∞–º–∏)
import java.time.LocalDate; // –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –∫–ª–∞—Å—Å –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –¥–∞—Ç–∞–º–∏
import java.util.ArrayList; // –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –¥–∏–Ω–∞–º–∏—á–µ—Å–∫–∏–π –º–∞—Å—Å–∏–≤ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Ä–∞—Å—Ö–æ–¥–æ–≤
import java.util.List; // –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å List

/**
 * –ö–ª–∞—Å—Å, —É–ø—Ä–∞–≤–ª—è—é—â–∏–π —Å–ø–∏—Å–∫–æ–º —Ä–∞—Å—Ö–æ–¥–æ–≤, –∏—Ö –æ–±—Ä–∞–±–æ—Ç–∫–æ–π –∏ –æ–ø–µ—Ä–∞—Ü–∏—è–º–∏ —Å —Ñ–∞–π–ª–∞–º–∏.
 */
public class ExpenseManager {
    private List<Expense> expenses; // –ü—Ä–∏–≤–∞—Ç–Ω–æ–µ –ø–æ–ª–µ: –°–ø–∏—Å–æ–∫ –æ–±—ä–µ–∫—Ç–æ–≤ Expense, —Ö—Ä–∞–Ω—è—â–∏—Ö—Å—è –≤ –ø–∞–º—è—Ç–∏.
    private final String FILE_NAME = "expenses.txt"; // –ö–æ–Ω—Å—Ç–∞–Ω—Ç–∞: –ò–º—è —Ñ–∞–π–ª–∞, –∏—Å–ø–æ–ª—å–∑—É–µ–º–æ–≥–æ –¥–ª—è —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è/–∑–∞–≥—Ä—É–∑–∫–∏.

    /**
     * –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä –∫–ª–∞—Å—Å–∞ ExpenseManager.
     * –í—ã–∑—ã–≤–∞–µ—Ç—Å—è –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –æ–±—ä–µ–∫—Ç–∞ –º–µ–Ω–µ–¥–∂–µ—Ä–∞.
     */
    public ExpenseManager() {
        this.expenses = new ArrayList<>(); // –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º —Å–ø–∏—Å–æ–∫ –∫–∞–∫ –ø—É—Å—Ç–æ–π ArrayList
        loadExpenses(); // –°—Ä–∞–∑—É –ø—ã—Ç–∞–µ–º—Å—è –∑–∞–≥—Ä—É–∑–∏—Ç—å –¥–∞–Ω–Ω—ã–µ –∏–∑ —Ñ–∞–π–ª–∞ –ø—Ä–∏ –∑–∞–ø—É—Å–∫–µ –ø—Ä–æ–≥—Ä–∞–º–º—ã.
    }

    // --- Data Operations (–û–ø–µ—Ä–∞—Ü–∏–∏ —Å –¥–∞–Ω–Ω—ã–º–∏) ---

    /**
     * –î–æ–±–∞–≤–ª—è–µ—Ç –Ω–æ–≤—ã–π –æ–±—ä–µ–∫—Ç Expense –≤ —Å–ø–∏—Å–æ–∫.
     * @param expense –û–±—ä–µ–∫—Ç, –∫–æ—Ç–æ—Ä—ã–π –Ω—É–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å.
     */
    public void addExpense(Expense expense) {
        expenses.add(expense); // –î–æ–±–∞–≤–ª–µ–Ω–∏–µ —ç–ª–µ–º–µ–Ω—Ç–∞ –≤ —Å–ø–∏—Å–æ–∫
        System.out.println("‚úÖ –†–∞—Å—Ö–æ–¥ —É—Å–ø–µ—à–Ω–æ –¥–æ–±–∞–≤–ª–µ–Ω."); // –í—ã–≤–æ–¥ —Å–æ–æ–±—â–µ–Ω–∏—è –æ–± —É—Å–ø–µ—Ö–µ
    }

    /**
     * –í—ã–≤–æ–¥–∏—Ç –≤—Å–µ —Ä–∞—Å—Ö–æ–¥—ã –∏–∑ —Å–ø–∏—Å–∫–∞ –≤ —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ–º –≤–∏–¥–µ.
     * –í–∫–ª—é—á–∞–µ—Ç —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫—É –∏ –≤—ã–≤–æ–¥ –∏—Ç–æ–≥–æ–≤–æ–π —Å—É–º–º—ã.
     */
    public void viewExpenses() {
        if (expenses.isEmpty()) { // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –ø—É—Å—Ç –ª–∏ —Å–ø–∏—Å–æ–∫
            System.out.println("–°–ø–∏—Å–æ–∫ —Ä–∞—Å—Ö–æ–¥–æ–≤ –ø—É—Å—Ç."); // –í—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ, –µ—Å–ª–∏ —Å–ø–∏—Å–æ–∫ –ø—É—Å—Ç
            return; // –í—ã—Ö–æ–¥–∏–º –∏–∑ –º–µ—Ç–æ–¥–∞
        }

        System.out.println("\n--- –°–ø–∏—Å–æ–∫ –†–∞—Å—Ö–æ–¥–æ–≤ (–í—Å–µ–≥–æ: " + expenses.size() + ") ---"); // –ó–∞–≥–æ–ª–æ–≤–æ–∫ —Å –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ–º
        System.out.println("+------------+-----------------+------------+");
        System.out.println("| –î–∞—Ç–∞       | –ö–∞—Ç–µ–≥–æ—Ä–∏—è       | –°—É–º–º–∞      |"); // –ó–∞–≥–æ–ª–æ–≤–∫–∏ –∫–æ–ª–æ–Ω–æ–∫
        System.out.println("+------------+-----------------+------------+");

        // –ò—Å–ø–æ–ª—å–∑—É–µ–º Stream API:
        expenses.stream()
                // –°–æ—Ä—Ç–∏—Ä—É–µ–º —Ä–∞—Å—Ö–æ–¥—ã –ø–æ –¥–∞—Ç–µ (–µ1 –∏ –µ2 - –¥–≤–∞ —Ä–∞—Å—Ö–æ–¥–∞ –¥–ª—è —Å—Ä–∞–≤–Ω–µ–Ω–∏—è)
                .sorted((e1, e2) -> e1.getDate().compareTo(e2.getDate()))
                .forEach(System.out::println); // –í—ã–≤–æ–¥–∏–º –∫–∞–∂–¥—ã–π –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Ä–∞—Å—Ö–æ–¥ (–≤—ã–∑—ã–≤–∞–µ—Ç—Å—è Expense.toString())

        System.out.println("+------------+-----------------+------------+");
        // –í—ã–≤–æ–¥–∏–º –∏—Ç–æ–≥–æ–≤—É—é —Å—É–º–º—É, –∏—Å–ø–æ–ª—å–∑—É—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –≤—ã–≤–æ–¥ %.2f
        System.out.printf("| –ò–¢–û–ì       |                 | %.2f —Ä.\n", calculateTotal());
        System.out.println("-------------------------------------------\n");
    }

    /**
     * –†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ—Ç –æ–±—â—É—é —Å—É–º–º—É –≤—Å–µ—Ö —Ä–∞—Å—Ö–æ–¥–æ–≤.
     * @return –û–±—â–∞—è —Å—É–º–º–∞ –≤—Å–µ—Ö amount –≤ —Å–ø–∏—Å–∫–µ.
     */
    public double calculateTotal() {
        // Stream API: –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å–ø–∏—Å–æ–∫ –≤ –ø–æ—Ç–æ–∫, –∑–∞—Ç–µ–º –º–∞–ø–ø–∏—Ä—É–µ–º –∫–∞–∂–¥—ã–π Expense –∫ –µ–≥–æ amount (getAmount) –∏ —Å—É–º–º–∏—Ä—É–µ–º.
        return expenses.stream().mapToDouble(Expense::getAmount).sum();
    }

    // --- File Operations (–û–ø–µ—Ä–∞—Ü–∏–∏ —Å —Ñ–∞–π–ª–∞–º–∏) ---

    /**
     * –°–æ—Ö—Ä–∞–Ω—è–µ—Ç —Ç–µ–∫—É—â–∏–π —Å–ø–∏—Å–æ–∫ —Ä–∞—Å—Ö–æ–¥–æ–≤ –≤ —Ñ–∞–π–ª FILE_NAME.
     * –ò—Å–ø–æ–ª—å–∑—É–µ—Ç PrintWriter –¥–ª—è —É–¥–æ–±–Ω–æ–π –∑–∞–ø–∏—Å–∏ —Å—Ç—Ä–æ–∫.
     */
    public void saveExpenses() {
        // try-with-resources: –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –∑–∞–∫—Ä—ã—Ç–∏–µ writer, –¥–∞–∂–µ –µ—Å–ª–∏ –ø—Ä–æ–∏–∑–æ–π–¥–µ—Ç –∏—Å–∫–ª—é—á–µ–Ω–∏–µ
        try (PrintWriter writer = new PrintWriter(new FileWriter(FILE_NAME))) {
            for (Expense expense : expenses) {
                // –ó–∞–ø–∏—Å—ã–≤–∞–µ–º –∫–∞–∂–¥—É—é –∑–∞–ø–∏—Å—å –≤ —Ñ–∞–π–ª, –∏—Å–ø–æ–ª—å–∑—É—è —Ñ–æ—Ä–º–∞—Ç toFileString()
                writer.println(expense.toFileString());
            }
            System.out.println("üíæ –î–∞–Ω–Ω—ã–µ —É—Å–ø–µ—à–Ω–æ —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã –≤ —Ñ–∞–π–ª: " + FILE_NAME); // –£—Å–ø–µ—à–Ω–æ–µ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ
        } catch (IOException e) {
            // –û–±—Ä–∞–±–æ—Ç–∫–∞ –∏—Å–∫–ª—é—á–µ–Ω–∏–π –≤–≤–æ–¥–∞/–≤—ã–≤–æ–¥–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, –Ω–µ—Ç –ø—Ä–∞–≤ –Ω–∞ –∑–∞–ø–∏—Å—å)
            System.err.println("‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–∏ –¥–∞–Ω–Ω—ã—Ö –≤ —Ñ–∞–π–ª: " + e.getMessage());
        }
    }

    /**
     * –ó–∞–≥—Ä—É–∂–∞–µ—Ç —Å–ø–∏—Å–æ–∫ —Ä–∞—Å—Ö–æ–¥–æ–≤ –∏–∑ —Ñ–∞–π–ª–∞ FILE_NAME –ø—Ä–∏ —Å—Ç–∞—Ä—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—ã.
     * –†–∞–∑–±–∏—Ä–∞–µ—Ç –∫–∞–∂–¥—É—é —Å—Ç—Ä–æ–∫—É –∏ —Å–æ–∑–¥–∞–µ—Ç –æ–±—ä–µ–∫—Ç—ã Expense.
     */
    public void loadExpenses() {
        File file = new File(FILE_NAME); // –°–æ–∑–¥–∞–µ–º –æ–±—ä–µ–∫—Ç File
        if (!file.exists()) { // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ —Ñ–∞–π–ª
            System.out.println("‚ÑπÔ∏è –§–∞–π–ª –¥–∞–Ω–Ω—ã—Ö –Ω–µ –Ω–∞–π–¥–µ–Ω. –ù–∞—á–∏–Ω–∞–µ–º —Å –ø—É—Å—Ç–æ–≥–æ —Å–ø–∏—Å–∫–∞."); // –°–æ–æ–±—â–µ–Ω–∏–µ, –µ—Å–ª–∏ —Ñ–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω
            return; // –í—ã—Ö–æ–¥–∏–º –∏–∑ –º–µ—Ç–æ–¥–∞, —Å–ø–∏—Å–æ–∫ –æ—Å—Ç–∞–µ—Ç—Å—è –ø—É—Å—Ç—ã–º
        }

        // try-with-resources: –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –∑–∞–∫—Ä—ã—Ç–∏–µ reader
        try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
            String line;
            int count = 0;
            while ((line = reader.readLine()) != null) { // –ß–∏—Ç–∞–µ–º —Ñ–∞–π–ª –ø–æ—Å—Ç—Ä–æ—á–Ω–æ, –ø–æ–∫–∞ –Ω–µ –¥–æ—Å—Ç–∏–≥–Ω–µ–º –∫–æ–Ω—Ü–∞ (null)
                String[] parts = line.split(","); // –†–∞–∑–±–∏–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É –Ω–∞ —á–∞—Å—Ç–∏, –∏—Å–ø–æ–ª—å–∑—É—è –∑–∞–ø—è—Ç—É—é –∫–∞–∫ —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å
                if (parts.length == 3) { // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –ø–æ–ª—É—á–∏–ª–∏ —Ä–æ–≤–Ω–æ 3 —á–∞—Å—Ç–∏ (—Å—É–º–º–∞, –∫–∞—Ç–µ–≥–æ—Ä–∏—è, –¥–∞—Ç–∞)
                    try {
                        // –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —á–∞—Å—Ç–∏ –æ–±—Ä–∞—Ç–Ω–æ –≤ –Ω—É–∂–Ω—ã–µ —Ç–∏–ø—ã
                        double amount = Double.parseDouble(parts[0]);
                        String category = parts[1];
                        LocalDate date = LocalDate.parse(parts[2]);

                        // –°–æ–∑–¥–∞–µ–º –æ–±—ä–µ–∫—Ç Expense –∏ –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ –≤ –æ—Å–Ω–æ–≤–Ω–æ–π —Å–ø–∏—Å–æ–∫
                        expenses.add(new Expense(amount, category, date));
                        count++;
                    } catch (NumberFormatException | java.time.format.DateTimeParseException e) {
                        // –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—à–∏–±–æ–∫, –µ—Å–ª–∏ –≤ —Å—Ç—Ä–æ–∫–µ —Ñ–∞–π–ª–∞ –Ω–µ–≤–µ—Ä–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ (–Ω–µ —á–∏—Å–ª–æ –∏–ª–∏ –Ω–µ–≤–µ—Ä–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç –¥–∞—Ç—ã)
                        System.err.println("  ‚ùóÔ∏è –ü—Ä–æ–ø—É—â–µ–Ω–∞ –Ω–µ–≤–µ—Ä–Ω–∞—è —Å—Ç—Ä–æ–∫–∞ –≤ —Ñ–∞–π–ª–µ: " + line);
                    }
                }
            }
            System.out.println("üì• –ó–∞–≥—Ä—É–∂–µ–Ω–æ " + count + " –∑–∞–ø–∏—Å–µ–π –∏–∑ —Ñ–∞–π–ª–∞ " + FILE_NAME); // –°–æ–æ–±—â–µ–Ω–∏–µ –æ–± —É—Å–ø–µ—à–Ω–æ–π –∑–∞–≥—Ä—É–∑–∫–µ
        } catch (IOException e) {
            // –ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞ –≤–≤–æ–¥–∞/–≤—ã–≤–æ–¥–∞ –ø—Ä–∏ —á—Ç–µ–Ω–∏–∏ —Ñ–∞–π–ª–∞
            System.err.println("‚ùå –ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ –¥–∞–Ω–Ω—ã—Ö: " + e.getMessage());
        }
    }
}
