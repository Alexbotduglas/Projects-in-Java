public class Main {

    /**
     * –ì–ª–∞–≤–Ω—ã–π –º–µ—Ç–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º—ã. –° –Ω–µ–≥–æ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ.
     */
    public static void main(String[] args) {
        // –°–æ–∑–¥–∞–Ω–∏–µ –æ–±—ä–µ–∫—Ç–∞ Scanner, —Å–≤—è–∑–∞–Ω–Ω–æ–≥–æ —Å–æ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–º –ø–æ—Ç–æ–∫–æ–º –≤–≤–æ–¥–∞ (System.in).
        Scanner scanner = new Scanner(System.in);

        // –°–æ–∑–¥–∞–Ω–∏–µ —ç–∫–∑–µ–º–ø–ª—è—Ä–∞ –º–µ–Ω–µ–¥–∂–µ—Ä–∞. –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –∑–∞–ø—É—Å–∫–∞–µ—Ç –∑–∞–≥—Ä—É–∑–∫—É –¥–∞–Ω–Ω—ã—Ö.
        PhoneBookManager manager = new PhoneBookManager();
        boolean running = true; // –§–ª–∞–≥ –¥–ª—è –∫–æ–Ω—Ç—Ä–æ–ª—è —Ü–∏–∫–ª–∞ –º–µ–Ω—é

        System.out.println("--- üìû –î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ –¢–µ–ª–µ—Ñ–æ–Ω–Ω—É—é –∫–Ω–∏–≥—É ---");

        // –ì–ª–∞–≤–Ω—ã–π —Ü–∏–∫–ª –ø—Ä–æ–≥—Ä–∞–º–º—ã: –æ–±–µ—Å–ø–µ—á–∏–≤–∞–µ—Ç –Ω–µ–ø—Ä–µ—Ä—ã–≤–Ω—É—é —Ä–∞–±–æ—Ç—É –º–µ–Ω—é –¥–æ –≤—ã–±–æ—Ä–∞ –≤—ã—Ö–æ–¥–∞.
        while (running) {
            displayMenu();

            // –ü—Ä–æ–≤–µ—Ä–∫–∞ –≤–≤–æ–¥–∞: –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç, —á—Ç–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–≤–µ–ª —á–∏—Å–ª–æ, –ø—Ä–µ–∂–¥–µ —á–µ–º –ø—ã—Ç–∞—Ç—å—Å—è –µ–≥–æ —Å—á–∏—Ç–∞—Ç—å.
            if (!scanner.hasNextInt()) {
                System.out.println("üö´ –ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π –≤–≤–æ–¥. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ –æ—Ç 1 –¥–æ 4.");
                scanner.next(); // –°–±—Ä–∞—Å—ã–≤–∞–µ–º –Ω–µ—á–∏—Å–ª–æ–≤–æ–π –≤–≤–æ–¥, —á—Ç–æ–±—ã –∏–∑–±–µ–∂–∞—Ç—å –±–µ—Å–∫–æ–Ω–µ—á–Ω–æ–≥–æ —Ü–∏–∫–ª–∞
                continue; // –ù–∞—á–∏–Ω–∞–µ–º —Ü–∏–∫–ª —Å –Ω–∞—á–∞–ª–∞ (–ø–æ–≤—Ç–æ—Ä–Ω—ã–π –ø–æ–∫–∞–∑ –º–µ–Ω—é)
            }

            int choice = scanner.nextInt(); // –°—á–∏—Ç—ã–≤–∞–µ–º —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ-–≤—ã–±–æ—Ä
            // –í–ê–ñ–ù–û: nextInt() —Å—á–∏—Ç—ã–≤–∞–µ—Ç —Ç–æ–ª—å–∫–æ —á–∏—Å–ª–æ, –æ—Å—Ç–∞–≤–ª—è—è —Å–∏–º–≤–æ–ª –ø–µ—Ä–µ–≤–æ–¥–∞ —Å—Ç—Ä–æ–∫–∏ (\n) –≤ –±—É—Ñ–µ—Ä–µ.
            // nextLine() –Ω–∏–∂–µ "—Å—ä–µ–¥–∞–µ—Ç" —ç—Ç–æ—Ç —Å–∏–º–≤–æ–ª, –ø—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–∞—è –æ—à–∏–±–∫–∏ –≤ –ø–æ—Å–ª–µ–¥—É—é—â–µ–º nextLine().
            scanner.nextLine();

            // –û–ø–µ—Ä–∞—Ç–æ—Ä switch: —á–∏—Å—Ç—ã–π —Å–ø–æ—Å–æ–± –≤–µ—Ç–≤–ª–µ–Ω–∏—è –ª–æ–≥–∏–∫–∏ –Ω–∞ –æ—Å–Ω–æ–≤–µ –≤—ã–±–æ—Ä–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.
            switch (choice) {
                case 1:
                    addNewContact(scanner, manager);
                    break;
                case 2:
                    manager.viewAllContacts();
                    break;
                case 3:
                    searchContact(scanner, manager);
                    break;
                case 4:
                    // –§–∏–Ω–∞–ª—å–Ω–æ–µ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –¥–∞–Ω–Ω—ã—Ö –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç, —á—Ç–æ –∏–∑–º–µ–Ω–µ–Ω–∏—è –Ω–µ –±—É–¥—É—Ç –ø–æ—Ç–µ—Ä—è–Ω—ã.
                    manager.saveContactsToFile();
                    running = false; // –í—ã—Ö–æ–¥ –∏–∑ —Ü–∏–∫–ª–∞
                    System.out.println("üëã –ü—Ä–æ–≥—Ä–∞–º–º–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞. –î–æ —Å–≤–∏–¥–∞–Ω–∏—è!");
                    break;
                default:
                    System.out.println("üö´ –ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è –∫–æ–º–∞–Ω–¥–∞. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤—ã–±–µ—Ä–∏—Ç–µ –ø—É–Ω–∫—Ç –æ—Ç 1 –¥–æ 4.");
            }
        }
        scanner.close(); // –ó–∞–∫—Ä—ã—Ç–∏–µ Scanner –¥–ª—è –ø—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–µ–Ω–∏—è —É—Ç–µ—á–∫–∏ —Ä–µ—Å—É—Ä—Å–æ–≤
    }

    /**
     * –ü—Ä–∏–≤–∞—Ç–Ω—ã–π —Å—Ç–∞—Ç–∏—á–µ—Å–∫–∏–π –º–µ—Ç–æ–¥ –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –æ–ø—Ü–∏–π –º–µ–Ω—é.
     * 'static' - –º–æ–∂–µ—Ç –±—ã—Ç—å –≤—ã–∑–≤–∞–Ω –±–µ–∑ —Å–æ–∑–¥–∞–Ω–∏—è –æ–±—ä–µ–∫—Ç–∞ Main.
     * 'private' - –º–µ—Ç–æ–¥ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —Ç–æ–ª—å–∫–æ –≤–Ω—É—Ç—Ä–∏ —ç—Ç–æ–≥–æ –∫–ª–∞—Å—Å–∞.
     */
    private static void displayMenu() {
        System.out.println("\n----------------------------------------");
        System.out.println("1. ‚ûï –î–æ–±–∞–≤–∏—Ç—å –∫–æ–Ω—Ç–∞–∫—Ç");
        System.out.println("2. üìÑ –ü—Ä–æ—Å–º–æ—Ç—Ä–µ—Ç—å –≤—Å–µ –∫–æ–Ω—Ç–∞–∫—Ç—ã");
        System.out.println("3. üîç –ù–∞–π—Ç–∏ –∫–æ–Ω—Ç–∞–∫—Ç –ø–æ –∏–º–µ–Ω–∏");
        System.out.println("4. üö™ –í—ã—Ö–æ–¥ (–∏ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ)");
        System.out.print(">>> –í–∞—à –≤—ã–±–æ—Ä: ");
    }

    /**
     * –õ–æ–≥–∏–∫–∞ –ø–æ–ª—É—á–µ–Ω–∏—è –¥–∞–Ω–Ω—ã—Ö –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏ —Å–æ–∑–¥–∞–Ω–∏—è –Ω–æ–≤–æ–≥–æ –∫–æ–Ω—Ç–∞–∫—Ç–∞.
     */
    private static void addNewContact(Scanner scanner, PhoneBookManager manager) {
        System.out.print("–í–≤–µ–¥–∏—Ç–µ –∏–º—è: ");
        // trim() —É–¥–∞–ª—è–µ—Ç –Ω–∞—á–∞–ª—å–Ω—ã–µ –∏ –∫–æ–Ω–µ—á–Ω—ã–µ –ø—Ä–æ–±–µ–ª—ã.
        String name = scanner.nextLine().trim();

        System.out.print("–í–≤–µ–¥–∏—Ç–µ –Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞: ");
        String phone = scanner.nextLine().trim();

        // –ë–∞–∑–æ–≤–∞—è –≤–∞–ª–∏–¥–∞—Ü–∏—è –≤–≤–æ–¥–∞
        if (name.isEmpty() || phone.isEmpty()) {
            System.out.println("üö´ –ò–º—è –∏ –Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞ –Ω–µ –º–æ–≥—É—Ç –±—ã—Ç—å –ø—É—Å—Ç—ã–º–∏.");
            return;
        }

        Contact newContact = new Contact(name, phone); // –°–æ–∑–¥–∞–Ω–∏–µ –æ–±—ä–µ–∫—Ç–∞
        manager.addContact(newContact); // –ü–µ—Ä–µ–¥–∞—á–∞ –æ–±—ä–µ–∫—Ç–∞ –º–µ–Ω–µ–¥–∂–µ—Ä—É
    }

    /**
     * –õ–æ–≥–∏–∫–∞ –ø–æ–ª—É—á–µ–Ω–∏—è –ø–æ–∏—Å–∫–æ–≤–æ–≥–æ –∑–∞–ø—Ä–æ—Å–∞ –∏ –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤.
     */
    private static void searchContact(Scanner scanner, PhoneBookManager manager) {
        System.out.print("–í–≤–µ–¥–∏—Ç–µ —á–∞—Å—Ç—å –∏–º–µ–Ω–∏ –¥–ª—è –ø–æ–∏—Å–∫–∞: ");
        String searchName = scanner.nextLine().trim();
        
        // –ü–æ–ª—É—á–µ–Ω–∏–µ —Å–ø–∏—Å–∫–∞ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π
        List<Contact> foundContacts = manager.findContact(searchName);

        if (foundContacts.isEmpty()) {
            System.out.println("ü§∑‚Äç‚ôÄÔ∏è –ö–æ–Ω—Ç–∞–∫—Ç—ã –ø–æ –∑–∞–ø—Ä–æ—Å—É '" + searchName + "' –Ω–µ –Ω–∞–π–¥–µ–Ω—ã.");
            return;
        }

        // –í—ã–≤–æ–¥ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤ —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º —Ü–∏–∫–ª–∞
        System.out.println("\n--- –†–µ–∑—É–ª—å—Ç–∞—Ç—ã –ø–æ–∏—Å–∫–∞ –ø–æ '" + searchName + "' (–ù–∞–π–¥–µ–Ω–æ: " + foundContacts.size() + ") ---");
        for (Contact contact : foundContacts) {
            System.out.println(contact); // –í—ã–∑–æ–≤ Contact.toString()
        }
        System.out.println("----------------------------------------");
    }
}
