import java.io.*; // –ò–º–ø–æ—Ä—Ç –∫–ª–∞—Å—Å–æ–≤ –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –≤–≤–æ–¥–æ–º/–≤—ã–≤–æ–¥–æ–º (—Ñ–∞–π–ª–∞–º–∏)
import java.util.ArrayList; // –ò–º–ø–æ—Ä—Ç –¥–∏–Ω–∞–º–∏—á–µ—Å–∫–æ–≥–æ —Å–ø–∏—Å–∫–∞ (–∫–æ–ª–ª–µ–∫—Ü–∏–∏)
import java.util.List; // –ò–º–ø–æ—Ä—Ç –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞ List
import java.util.stream.Collectors; // –ò–º–ø–æ—Ä—Ç –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å–æ Stream API

/**
 * –ö–ª–∞—Å—Å PhoneBookManager —É–ø—Ä–∞–≤–ª—è–µ—Ç –∫–æ–ª–ª–µ–∫—Ü–∏–µ–π –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤ –∏
 * –æ—Ç–≤–µ—á–∞–µ—Ç –∑–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ/–∑–∞–≥—Ä—É–∑–∫—É –¥–∞–Ω–Ω—ã—Ö –≤/–∏–∑ —Ñ–∞–π–ª–∞.
 */
public class PhoneBookManager {

    // –°–ø–∏—Å–æ–∫ (–∫–æ–ª–ª–µ–∫—Ü–∏—è) –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –≤—Å–µ—Ö –æ–±—ä–µ–∫—Ç–æ–≤ Contact –≤ –æ–ø–µ—Ä–∞—Ç–∏–≤–Ω–æ–π –ø–∞–º—è—Ç–∏.
    private List<Contact> contacts;

    // –ö–æ–Ω—Å—Ç–∞–Ω—Ç—ã, –æ–ø—Ä–µ–¥–µ–ª—è—é—â–∏–µ –∏–º—è —Ñ–∞–π–ª–∞ –∏ —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å –¥–∞–Ω–Ω—ã—Ö.
    private static final String FILENAME = "phonebook.txt";
    private static final String FIELD_SEPARATOR = ";";

    /**
     * –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä PhoneBookManager.
     * –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ—Ç —Å–ø–∏—Å–æ–∫ –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤ –∏ —Å—Ä–∞–∑—É –∂–µ –ø—ã—Ç–∞–µ—Ç—Å—è –∑–∞–≥—Ä—É–∑–∏—Ç—å
     * —Å–æ—Ö—Ä–∞–Ω–µ–Ω–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ –∏–∑ —Ñ–∞–π–ª–∞.
     */
    public PhoneBookManager() {
        this.contacts = new ArrayList<>();
        loadContactsFromFile();
    }

    /**
     * –î–æ–±–∞–≤–ª—è–µ—Ç –Ω–æ–≤—ã–π –æ–±—ä–µ–∫—Ç Contact –≤ –∫–æ–ª–ª–µ–∫—Ü–∏—é.
     * @param contact –û–±—ä–µ–∫—Ç Contact, –∫–æ—Ç–æ—Ä—ã–π –Ω—É–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å.
     */
    public void addContact(Contact contact) {
        contacts.add(contact);
        System.out.println("‚úÖ –ö–æ–Ω—Ç–∞–∫—Ç '" + contact.getName() + "' –¥–æ–±–∞–≤–ª–µ–Ω.");
    }

    /**
     * –í—ã–≤–æ–¥–∏—Ç —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤, —Ö—Ä–∞–Ω—è—â–∏—Ö—Å—è –≤ –ø–∞–º—è—Ç–∏.
     */
    public void viewAllContacts() {
        if (contacts.isEmpty()) {
            System.out.println("ü§∑‚Äç‚ôÄÔ∏è –¢–µ–ª–µ—Ñ–æ–Ω–Ω–∞—è –∫–Ω–∏–≥–∞ –ø—É—Å—Ç–∞.");
            return;
        }

        System.out.println("\n--- –°–ø–∏—Å–æ–∫ –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤ (–í—Å–µ–≥–æ: " + contacts.size() + ") ---");
        // –¶–∏–∫–ª for-each –¥–ª—è –ø–µ—Ä–µ–±–æ—Ä–∞ –∏ –≤—ã–≤–æ–¥–∞ –∫–∞–∂–¥–æ–≥–æ –∫–æ–Ω—Ç–∞–∫—Ç–∞ (–∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è Contact.toString())
        for (Contact contact : contacts) {
            System.out.println(contact);
        }
        System.out.println("----------------------------------------");
    }

    /**
     * –ò—â–µ—Ç –∫–æ–Ω—Ç–∞–∫—Ç—ã –ø–æ –∏–º–µ–Ω–∏ (—á–∞—Å—Ç–∏—á–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ, –±–µ–∑ —É—á–µ—Ç–∞ —Ä–µ–≥–∏—Å—Ç—Ä–∞).
     * @param searchName –°—Ç—Ä–æ–∫–∞ –¥–ª—è –ø–æ–∏—Å–∫–∞.
     * @return –°–ø–∏—Å–æ–∫ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö –æ–±—ä–µ–∫—Ç–æ–≤ Contact.
     */
    public List<Contact> findContact(String searchName) {
        String lowerCaseSearchName = searchName.toLowerCase();

        // –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ Stream API:
        // 1. stream(): –ø—Ä–µ–≤—Ä–∞—â–∞–µ—Ç —Å–ø–∏—Å–æ–∫ –≤ –ø–æ—Ç–æ–∫ –¥–∞–Ω–Ω—ã—Ö.
        // 2. filter(): –æ—Å—Ç–∞–≤–ª—è–µ—Ç —Ç–æ–ª—å–∫–æ —Ç–µ –∫–æ–Ω—Ç–∞–∫—Ç—ã, –∏–º–µ–Ω–∞ –∫–æ—Ç–æ—Ä—ã—Ö (–≤ –Ω–∏–∂–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ)
        //    —Å–æ–¥–µ—Ä–∂–∞—Ç –∏—Å–∫–æ–º—É—é —Å—Ç—Ä–æ–∫—É (–≤ –Ω–∏–∂–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ).
        // 3. collect(Collectors.toList()): —Å–æ–±–∏—Ä–∞–µ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç –æ–±—Ä–∞—Ç–Ω–æ –≤ –Ω–æ–≤—ã–π List.
        return contacts.stream()
                .filter(c -> c.getName().toLowerCase().contains(lowerCaseSearchName))
                .collect(Collectors.toList());
    }

    /**
     * –°–æ—Ö—Ä–∞–Ω—è–µ—Ç —Ç–µ–∫—É—â–∏–π —Å–ø–∏—Å–æ–∫ –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤ –≤ —Ç–µ–∫—Å—Ç–æ–≤—ã–π —Ñ–∞–π–ª.
     * –ò—Å–ø–æ–ª—å–∑—É–µ—Ç FileWriter (–¥–ª—è –∑–∞–ø–∏—Å–∏) –∏ PrintWriter (–¥–ª—è —É–¥–æ–±–Ω–æ–π –∑–∞–ø–∏—Å–∏ —Å—Ç—Ä–æ–∫).
     */
    public void saveContactsToFile() {
        // –ö–æ–Ω—Å—Ç—Ä—É–∫—Ü–∏—è try-with-resources: –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç, —á—Ç–æ PrintWriter –∏ FileWriter
        // –±—É–¥—É—Ç –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –∑–∞–∫—Ä—ã—Ç—ã, –¥–∞–∂–µ –µ—Å–ª–∏ –ø—Ä–æ–∏–∑–æ–π–¥–µ—Ç –∏—Å–∫–ª—é—á–µ–Ω–∏–µ (IOException).
        try (PrintWriter writer = new PrintWriter(new FileWriter(FILENAME))) {
            for (Contact contact : contacts) {
                // –ó–∞–ø–∏—Å—ã–≤–∞–µ–º —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω—É—é —Å—Ç—Ä–æ–∫—É –∫–æ–Ω—Ç–∞–∫—Ç–∞, —Å–æ–∑–¥–∞–Ω–Ω—É—é –º–µ—Ç–æ–¥–æ–º toFileString(),
                // –∏ –ø–µ—Ä–µ—Ö–æ–¥–∏–º –Ω–∞ –Ω–æ–≤—É—é —Å—Ç—Ä–æ–∫—É (println).
                writer.println(contact.toFileString());
            }
            System.out.println("\nüíæ –î–∞–Ω–Ω—ã–µ —É—Å–ø–µ—à–Ω–æ —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã –≤ —Ñ–∞–π–ª: " + FILENAME);
        } catch (IOException e) {
            // –û–±—Ä–∞–±–æ—Ç–∫–∞ –∏—Å–∫–ª—é—á–µ–Ω–∏—è, –µ—Å–ª–∏ –ø—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –≤–≤–æ–¥–∞/–≤—ã–≤–æ–¥–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, –Ω–µ—Ç –ø—Ä–∞–≤ –Ω–∞ –∑–∞–ø–∏—Å—å)
            System.err.println("‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–∏ –¥–∞–Ω–Ω—ã—Ö: " + e.getMessage());
        }
    }

    /**
     * –ó–∞–≥—Ä—É–∂–∞–µ—Ç –∫–æ–Ω—Ç–∞–∫—Ç—ã –∏–∑ —Ç–µ–∫—Å—Ç–æ–≤–æ–≥–æ —Ñ–∞–π–ª–∞ –≤ –ø–∞–º—è—Ç—å.
     * –ò—Å–ø–æ–ª—å–∑—É–µ—Ç FileReader (–¥–ª—è —á—Ç–µ–Ω–∏—è) –∏ BufferedReader (–¥–ª—è —É–¥–æ–±–Ω–æ–≥–æ –ø–æ—Å—Ç—Ä–æ—á–Ω–æ–≥–æ —á—Ç–µ–Ω–∏—è).
     */
    public void loadContactsFromFile() {
        File file = new File(FILENAME);
        // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ —Ñ–∞–π–ª
        if (!file.exists()) {
            System.out.println("‚ÑπÔ∏è –§–∞–π–ª –¥–∞–Ω–Ω—ã—Ö –Ω–µ –Ω–∞–π–¥–µ–Ω. –°–æ–∑–¥–∞–Ω–∞ –ø—É—Å—Ç–∞—è –∫–Ω–∏–≥–∞.");
            return;
        }

        // try-with-resources —Å BufferedReader –¥–ª—è —ç—Ñ—Ñ–µ–∫—Ç–∏–≤–Ω–æ–≥–æ –ø–æ—Å—Ç—Ä–æ—á–Ω–æ–≥–æ —á—Ç–µ–Ω–∏—è
        try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
            String line;
            int loadedCount = 0;
            // –¶–∏–∫–ª —á—Ç–µ–Ω–∏—è: —Å—á–∏—Ç—ã–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É –∑–∞ —Å—Ç—Ä–æ–∫–æ–π, –ø–æ–∫–∞ –Ω–µ –¥–æ—Å—Ç–∏–≥–Ω–µ–º –∫–æ–Ω—Ü–∞ —Ñ–∞–π–ª–∞ (line == null)
            while ((line = reader.readLine()) != null) {
                // –†–∞–∑–¥–µ–ª—è–µ–º —Å—Ç—Ä–æ–∫—É –Ω–∞ –º–∞—Å—Å–∏–≤ –ø–æ–¥—Å—Ç—Ä–æ–∫ –ø–æ —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—é ";"
                String[] parts = line.split(FIELD_SEPARATOR);

                // –ü—Ä–æ–≤–µ—Ä–∫–∞: —É–±–µ–∂–¥–∞–µ–º—Å—è, —á—Ç–æ —Å—Ç—Ä–æ–∫–∞ —Å–æ–¥–µ—Ä–∂–∏—Ç —Ä–æ–≤–Ω–æ –¥–≤–µ —á–∞—Å—Ç–∏ (–ò–º—è –∏ –ù–æ–º–µ—Ä)
                if (parts.length == 2) {
                    String name = parts[0];
                    String phoneNumber = parts[1];
                    // –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—ã–π –æ–±—ä–µ–∫—Ç Contact –∏ –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ –≤ —Å–ø–∏—Å–æ–∫
                    contacts.add(new Contact(name, phoneNumber));
                    loadedCount++;
                } else {
                    System.err.println("‚ö†Ô∏è –ü—Ä–æ–ø—É—â–µ–Ω–∞ –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω–∞—è —Å—Ç—Ä–æ–∫–∞ –≤ —Ñ–∞–π–ª–µ: " + line);
                }
            }
            System.out.println("‚úÖ –ó–∞–≥—Ä—É–∂–µ–Ω–æ " + loadedCount + " –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤ –∏–∑ —Ñ–∞–π–ª–∞: " + FILENAME);
        } catch (FileNotFoundException e) {
            // –≠—Ç–æ –∏—Å–∫–ª—é—á–µ–Ω–∏–µ —Ç–µ–æ—Ä–µ—Ç–∏—á–µ—Å–∫–∏ –Ω–µ –¥–æ–ª–∂–Ω–æ —Å—Ä–∞–±–æ—Ç–∞—Ç—å –±–ª–∞–≥–æ–¥–∞—Ä—è file.exists(), –Ω–æ –ª—É—á—à–µ –µ–≥–æ –æ–±—Ä–∞–±–æ—Ç–∞—Ç—å
            System.err.println("‚ùå –û—à–∏–±–∫–∞: –§–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω, —Ö–æ—Ç—è —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏–µ –ø—Ä–æ–≤–µ—Ä—è–ª–æ—Å—å.");
        } catch (IOException e) {
            System.err.println("‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ —á—Ç–µ–Ω–∏–∏ –¥–∞–Ω–Ω—ã—Ö: " + e.getMessage());
        }
    }
}
