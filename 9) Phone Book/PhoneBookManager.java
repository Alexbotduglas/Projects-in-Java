import java.io.*;          // –ò–º–ø–æ—Ä—Ç –≤—Å–µ—Ö –∫–ª–∞—Å—Å–æ–≤ –≤–≤–æ–¥–∞/–≤—ã–≤–æ–¥–∞ (I/O)
import java.util.ArrayList;  // –ò–º–ø–æ—Ä—Ç —Ä–µ–∞–ª–∏–∑–∞—Ü–∏–∏ –¥–∏–Ω–∞–º–∏—á–µ—Å–∫–æ–≥–æ –º–∞—Å—Å–∏–≤–∞
import java.util.List;       // –ò–º–ø–æ—Ä—Ç –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞ –∫–æ–ª–ª–µ–∫—Ü–∏–∏ List
import java.util.stream.Collectors; // –ò–º–ø–æ—Ä—Ç –¥–ª—è —Å–±–æ—Ä–∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤ Stream (—Å–æ–≤—Ä–µ–º–µ–Ω–Ω—ã–π –ø–æ–¥—Ö–æ–¥)

/**
 * –ö–ª–∞—Å—Å PhoneBookManager: –¶–µ–Ω—Ç—Ä–∞–ª—å–Ω—ã–π –∫–ª–∞—Å—Å, —É–ø—Ä–∞–≤–ª—è—é—â–∏–π —Å–æ—Å—Ç–æ—è–Ω–∏–µ–º —Ç–µ–ª–µ—Ñ–æ–Ω–Ω–æ–π –∫–Ω–∏–≥–∏.
 * * –û—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ—Å—Ç—å:
 * 1. –•—Ä–∞–Ω–µ–Ω–∏–µ –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤ –≤ –ø–∞–º—è—Ç–∏ (–∫–æ–ª–ª–µ–∫—Ü–∏—è).
 * 2. –†–µ–∞–ª–∏–∑–∞—Ü–∏—è –±–∏–∑–Ω–µ—Å-–ª–æ–≥–∏–∫–∏ (–¥–æ–±–∞–≤–ª–µ–Ω–∏–µ, –ø–æ–∏—Å–∫).
 * 3. –û–±–µ—Å–ø–µ—á–µ–Ω–∏–µ –ø–µ—Ä—Å–∏—Å—Ç–µ–Ω—Ç–Ω–æ—Å—Ç–∏ (—Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ/–∑–∞–≥—Ä—É–∑–∫–∞ –¥–∞–Ω–Ω—ã—Ö –≤/–∏–∑ —Ñ–∞–π–ª–∞).
 */
public class PhoneBookManager {

    // ---------------------- –ü–û–õ–Ø –∏ –ö–û–ù–°–¢–ê–ù–¢–´ ----------------------

    // –ö–æ–ª–ª–µ–∫—Ü–∏—è ArrayList: –í—ã–±—Ä–∞–Ω–∞ –∫–∞–∫ –æ—Å–Ω–æ–≤–Ω–æ–µ —Ö—Ä–∞–Ω–∏–ª–∏—â–µ, –ø–æ—Å–∫–æ–ª—å–∫—É –æ–±–µ—Å–ø–µ—á–∏–≤–∞–µ—Ç
    // –±—ã—Å—Ç—Ä—ã–π –¥–æ—Å—Ç—É–ø –ø–æ –∏–Ω–¥–µ–∫—Å—É –∏ —É–¥–æ–±–Ω–æ–µ –¥–æ–±–∞–≤–ª–µ–Ω–∏–µ —ç–ª–µ–º–µ–Ω—Ç–æ–≤.
    private List<Contact> contacts;

    // –ö–æ–Ω—Å—Ç–∞–Ω—Ç—ã (static final) –¥–ª—è –∏–∑–±–µ–∂–∞–Ω–∏—è "–º–∞–≥–∏—á–µ—Å–∫–∏—Ö —Å—Ç—Ä–æ–∫" –∏ —É–ø—Ä–æ—â–µ–Ω–∏—è –ø–æ–¥–¥–µ—Ä–∂–∫–∏.
    private static final String FILENAME = "phonebook.txt";
    private static final String FIELD_SEPARATOR = ";";

    // ---------------------- –ö–û–ù–°–¢–†–£–ö–¢–û–† ----------------------

    /**
     * –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä PhoneBookManager.
     * –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ—Ç –∫–æ–ª–ª–µ–∫—Ü–∏—é –∏ –∑–∞–ø—É—Å–∫–∞–µ—Ç –ø—Ä–æ—Ü–µ—Å—Å –∑–∞–≥—Ä—É–∑–∫–∏ –¥–∞–Ω–Ω—ã—Ö,
     * —á—Ç–æ–±—ã –∫–Ω–∏–≥–∞ –±—ã–ª–∞ –∞–∫—Ç—É–∞–ª—å–Ω–æ–π —Å—Ä–∞–∑—É –ø–æ—Å–ª–µ —Å—Ç–∞—Ä—Ç–∞ –ø—Ä–æ–≥—Ä–∞–º–º—ã.
     */
    public PhoneBookManager() {
        this.contacts = new ArrayList<>();
        // –í—ã–∑–æ–≤ –º–µ—Ç–æ–¥–∞ –∑–∞–≥—Ä—É–∑–∫–∏. –ï—Å–ª–∏ —Ñ–∞–π–ª–∞ –Ω–µ—Ç, –±—É–¥–µ—Ç —Å–æ–∑–¥–∞–Ω–∞ –ø—É—Å—Ç–∞—è –∫–Ω–∏–≥–∞.
        loadContactsFromFile();
    }

    // ---------------------- –ú–ï–¢–û–î–´ –ë–ò–ó–ù–ï–°-–õ–û–ì–ò–ö–ò ----------------------

    /**
     * –î–æ–±–∞–≤–ª—è–µ—Ç –Ω–æ–≤—ã–π –∫–æ–Ω—Ç–∞–∫—Ç –≤ –∫–æ–ª–ª–µ–∫—Ü–∏—é 'contacts'.
     * @param contact –û–±—ä–µ–∫—Ç Contact, –∫–æ—Ç–æ—Ä—ã–π –Ω—É–∂–Ω–æ —Å–æ—Ö—Ä–∞–Ω–∏—Ç—å –≤ –ø–∞–º—è—Ç–∏.
     */
    public void addContact(Contact contact) {
        contacts.add(contact);
        System.out.println("‚úÖ –ö–æ–Ω—Ç–∞–∫—Ç '" + contact.getName() + "' –¥–æ–±–∞–≤–ª–µ–Ω.");
    }

    /**
     * –í—ã–≤–æ–¥–∏—Ç —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤.
     */
    public void viewAllContacts() {
        if (contacts.isEmpty()) {
            System.out.println("ü§∑‚Äç‚ôÄÔ∏è –¢–µ–ª–µ—Ñ–æ–Ω–Ω–∞—è –∫–Ω–∏–≥–∞ –ø—É—Å—Ç–∞.");
            return; // –ü—Ä–µ—Ä—ã–≤–∞–Ω–∏–µ –º–µ—Ç–æ–¥–∞, –µ—Å–ª–∏ —Å–ø–∏—Å–æ–∫ –ø—É—Å—Ç.
        }

        System.out.println("\n--- –°–ø–∏—Å–æ–∫ –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤ (–í—Å–µ–≥–æ: " + contacts.size() + ") ---");
        // –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ —Ü–∏–∫–ª–∞ for-each - –∏–¥–∏–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–π —Å–ø–æ—Å–æ–± –ø–µ—Ä–µ–±–æ—Ä–∞ –∫–æ–ª–ª–µ–∫—Ü–∏–π –≤ Java.
        for (Contact contact : contacts) {
            // –ó–¥–µ—Å—å –Ω–µ—è–≤–Ω–æ –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è –º–µ—Ç–æ–¥ contact.toString()
            System.out.println(contact);
        }
        System.out.println("----------------------------------------");
    }

    /**
     * –ò—â–µ—Ç –∫–æ–Ω—Ç–∞–∫—Ç—ã –ø–æ —Å–æ–≤–ø–∞–¥–µ–Ω–∏—é (–±–µ–∑ —É—á–µ—Ç–∞ —Ä–µ–≥–∏—Å—Ç—Ä–∞) —á–∞—Å—Ç–∏ –∏–º–µ–Ω–∏.
     * @param searchName –°—Ç—Ä–æ–∫–∞ –ø–æ–∏—Å–∫–∞, –≤–≤–µ–¥–µ–Ω–Ω–∞—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º.
     * @return –°–ø–∏—Å–æ–∫ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤ (–º–æ–∂–µ—Ç –±—ã—Ç—å –ø—É—Å—Ç—ã–º).
     */
    public List<Contact> findContact(String searchName) {
        String lowerCaseSearchName = searchName.toLowerCase();

        // –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ Stream API (Java 8+):
        // 1. contacts.stream(): –°–æ–∑–¥–∞–Ω–∏–µ –ø–æ—Ç–æ–∫–∞ –¥–∞–Ω–Ω—ã—Ö.
        // 2. .filter(c -> ...): –§–∏–ª—å—Ç—Ä–∞—Ü–∏—è –ø–æ —É—Å–ª–æ–≤–∏—é. –£—Å–ª–æ–≤–∏–µ: –∏–º—è –∫–æ–Ω—Ç–∞–∫—Ç–∞ (–≤ –Ω–∏–∂–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ)
        //    –¥–æ–ª–∂–Ω–æ —Å–æ–¥–µ—Ä–∂–∞—Ç—å –≤–≤–µ–¥–µ–Ω–Ω—É—é —Å—Ç—Ä–æ–∫—É –ø–æ–∏—Å–∫–∞ (–≤ –Ω–∏–∂–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ).
        // 3. .collect(Collectors.toList()): –°–±–æ—Ä –æ—Ç—Ñ–∏–ª—å—Ç—Ä–æ–≤–∞–Ω–Ω—ã—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤ –Ω–æ–≤—ã–π —Å–ø–∏—Å–æ–∫.
        return contacts.stream()
                .filter(c -> c.getName().toLowerCase().contains(lowerCaseSearchName))
                .collect(Collectors.toList());
    }

    // ---------------------- –ú–ï–¢–û–î–´ –§–ê–ô–õ–û–í–û–ì–û I/O (–°–û–•–†–ê–ù–ï–ù–ò–ï) ----------------------

    /**
     * –°–æ—Ö—Ä–∞–Ω—è–µ—Ç —Ç–µ–∫—É—â–µ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ –∫–æ–ª–ª–µ–∫—Ü–∏–∏ 'contacts' –≤ —Ç–µ–∫—Å—Ç–æ–≤—ã–π —Ñ–∞–π–ª.
     * –ò—Å–ø–æ–ª—å–∑—É–µ—Ç PrintWriter –ø–æ–≤–µ—Ä—Ö FileWriter –¥–ª—è —É–¥–æ–±–Ω–æ–π –∑–∞–ø–∏—Å–∏ —Å—Ç—Ä–æ–∫.
     */
    public void saveContactsToFile() {
        // –ë–ª–æ–∫ try-with-resources:
        // –≠—Ç–æ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π –ø–∞—Ç—Ç–µ—Ä–Ω Java –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å —Ä–µ—Å—É—Ä—Å–∞–º–∏, –∫–æ—Ç–æ—Ä—ã–µ –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å –∑–∞–∫—Ä—ã—Ç—ã
        // (—Ç–∞–∫–∏–º–∏ –∫–∞–∫ —Ñ–∞–π–ª—ã, —Å–æ–µ–¥–∏–Ω–µ–Ω–∏—è). –û–Ω –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –≤—ã–∑—ã–≤–∞–µ—Ç –º–µ—Ç–æ–¥ close()
        // —É —Ä–µ—Å—É—Ä—Å–æ–≤ (writer, new FileWriter(FILENAME)) –ø–æ –∑–∞–≤–µ—Ä—à–µ–Ω–∏–∏ –±–ª–æ–∫–∞ try –∏–ª–∏ –ø—Ä–∏ –æ—à–∏–±–∫–µ.
        try (PrintWriter writer = new PrintWriter(new FileWriter(FILENAME))) {
            for (Contact contact : contacts) {
                // –ó–∞–ø–∏—Å—ã–≤–∞–µ–º –æ–¥–Ω—É —Å—Ç—Ä–æ–∫—É –¥–∞–Ω–Ω—ã—Ö –∫–æ–Ω—Ç–∞–∫—Ç–∞ –≤ —Ñ–∞–π–ª, –∏—Å–ø–æ–ª—å–∑—É—è –ø—Ä–∏–Ω—è—Ç—ã–π —Ñ–æ—Ä–º–∞—Ç.
                writer.println(contact.toFileString());
            }
            System.out.println("\nüíæ –î–∞–Ω–Ω—ã–µ —É—Å–ø–µ—à–Ω–æ —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã –≤ —Ñ–∞–π–ª: " + FILENAME);
        } catch (IOException e) {
            // –û–±—Ä–∞–±–æ—Ç–∫–∞ –∏—Å–∫–ª—é—á–µ–Ω–∏—è: IOException - —ç—Ç–æ –æ–±—â–µ–µ –∏—Å–∫–ª—é—á–µ–Ω–∏–µ –¥–ª—è –ø—Ä–æ–±–ª–µ–º –≤–≤–æ–¥–∞/–≤—ã–≤–æ–¥–∞.
            System.err.println("‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–∏ –¥–∞–Ω–Ω—ã—Ö: " + e.getMessage());
        }
    }

    // ---------------------- –ú–ï–¢–û–î–´ –§–ê–ô–õ–û–í–û–ì–û I/O (–ó–ê–ì–†–£–ó–ö–ê) ----------------------

    /**
     * –ó–∞–≥—Ä—É–∂–∞–µ—Ç –∫–æ–Ω—Ç–∞–∫—Ç—ã –∏–∑ —Ñ–∞–π–ª–∞, –ø–∞—Ä—Å–∏—Ç —Å—Ç—Ä–æ–∫–∏ –∏ –≤–æ—Å—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç –∫–æ–ª–ª–µ–∫—Ü–∏—é 'contacts'.
     * –ò—Å–ø–æ–ª—å–∑—É–µ—Ç BufferedReader –ø–æ–≤–µ—Ä—Ö FileReader –¥–ª—è —ç—Ñ—Ñ–µ–∫—Ç–∏–≤–Ω–æ–≥–æ –ø–æ—Å—Ç—Ä–æ—á–Ω–æ–≥–æ —á—Ç–µ–Ω–∏—è.
     */
    public void loadContactsFromFile() {
        File file = new File(FILENAME);
        // –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ —Ñ–∞–π–ª–∞, —á—Ç–æ–±—ã –∏–∑–±–µ–∂–∞—Ç—å FileNotFoundException –∏ —Å–æ–∑–¥–∞—Ç—å –∫–Ω–∏–≥—É,
        // –µ—Å–ª–∏ –¥–∞–Ω–Ω—ã–µ –µ—â–µ –Ω–µ –±—ã–ª–∏ —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã.
        if (!file.exists()) {
            System.out.println("‚ÑπÔ∏è –§–∞–π–ª –¥–∞–Ω–Ω—ã—Ö –Ω–µ –Ω–∞–π–¥–µ–Ω. –°–æ–∑–¥–∞–Ω–∞ –ø—É—Å—Ç–∞—è –∫–Ω–∏–≥–∞.");
            return;
        }

        // try-with-resources —Å BufferedReader: –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –∑–∞–∫—Ä—ã—Ç–∏–µ,
        // –∞ BufferedReader –æ–±–µ—Å–ø–µ—á–∏–≤–∞–µ—Ç –±—ã—Å—Ç—Ä—É—é (–±—É—Ñ–µ—Ä–∏–∑–∏—Ä–æ–≤–∞–Ω–Ω—É—é) –∏ —É–¥–æ–±–Ω—É—é –ø–æ—Å—Ç—Ä–æ—á–Ω—É—é –æ–±—Ä–∞–±–æ—Ç–∫—É.
        try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
            String line;
            int loadedCount = 0;

            // –¶–∏–∫–ª —á—Ç–µ–Ω–∏—è: reader.readLine() —Å—á–∏—Ç—ã–≤–∞–µ—Ç –æ–¥–Ω—É —Å—Ç—Ä–æ–∫—É –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –µ–µ.
            // –¶–∏–∫–ª –ø—Ä–æ–¥–æ–ª–∂–∞–µ—Ç—Å—è, –ø–æ–∫–∞ –Ω–µ –±—É–¥–µ—Ç –¥–æ—Å—Ç–∏–≥–Ω—É—Ç –∫–æ–Ω–µ—Ü —Ñ–∞–π–ª–∞ (readLine() –≤–µ—Ä–Ω–µ—Ç null).
            while ((line = reader.readLine()) != null) {
                // –ú–µ—Ç–æ–¥ split() —Ä–∞–∑–¥–µ–ª—è–µ—Ç —Å—Ç—Ä–æ–∫—É –Ω–∞ –º–∞—Å—Å–∏–≤ –ø–æ–¥—Å—Ç—Ä–æ–∫, –∏—Å–ø–æ–ª—å–∑—É—è FIELD_SEPARATOR.
                String[] parts = line.split(FIELD_SEPARATOR);

                // –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ —Ü–µ–ª–æ—Å—Ç–Ω–æ—Å—Ç—å –¥–∞–Ω–Ω—ã—Ö: –º—ã –æ–∂–∏–¥–∞–µ–º —Ä–æ–≤–Ω–æ –¥–≤–µ —á–∞—Å—Ç–∏ (–ò–º—è, –ù–æ–º–µ—Ä).
                if (parts.length == 2) {
                    String name = parts[0];
                    String phoneNumber = parts[1];
                    // –í–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–µ –æ–±—ä–µ–∫—Ç–∞ Contact –∏–∑ –¥–∞–Ω–Ω—ã—Ö —Ñ–∞–π–ª–∞.
                    contacts.add(new Contact(name, phoneNumber));
                    loadedCount++;
                } else {
                    // –û–±—Ä–∞–±–æ—Ç–∫–∞ –ø–æ–≤—Ä–µ–∂–¥–µ–Ω–Ω–æ–π —Å—Ç—Ä–æ–∫–∏ (–ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ –æ—à–∏–±–∫–∏)
                    System.err.println("‚ö†Ô∏è –ü—Ä–æ–ø—É—â–µ–Ω–∞ –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω–∞—è —Å—Ç—Ä–æ–∫–∞ –≤ —Ñ–∞–π–ª–µ: " + line);
                }
            }
            System.out.println("‚úÖ –ó–∞–≥—Ä—É–∂–µ–Ω–æ " + loadedCount + " –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤ –∏–∑ —Ñ–∞–π–ª–∞: " + FILENAME);
        } catch (IOException e) {
            // –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—à–∏–±–∫–∏, –≤–æ–∑–Ω–∏–∫–∞—é—â–µ–π –ø—Ä–∏ –ø—Ä–æ–±–ª–µ–º–∞—Ö —Å —á—Ç–µ–Ω–∏–µ–º —Ñ–∞–π–ª–∞.
            System.err.println("‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ –¥–∞–Ω–Ω—ã—Ö: " + e.getMessage());
        }
    }
}
