/**
 * Класс Contact: Модель данных, представляющая собой единичный контакт в телефонной книге.
 * * Назначение: Обеспечивает структуру данных для хранения имени и номера телефона,
 * а также реализует принципы инкапсуляции (один из столпов ООП).
 */
public class Contact {

    // ---------------------- АТРИБУТЫ (ПОЛЯ) ----------------------

    // Ключевое слово 'private' обеспечивает инкапсуляцию: прямой доступ к этим полям
    // возможен только внутри этого класса. Это защищает данные от неконтролируемого
    // изменения извне.
    private String name;
    private String phoneNumber;

    // ---------------------- КОНСТРУКТОР ----------------------

    /**
     * Конструктор класса Contact. Используется для инициализации нового объекта
     * с заданными именем и номером телефона при его создании (instantiation).
     * * @param name Имя контакта.
     * @param phoneNumber Номер телефона контакта.
     */
    public Contact(String name, String phoneNumber) {
        // 'this.name' ссылается на поле класса; 'name' - на параметр метода.
        this.name = name;
        this.phoneNumber = phoneNumber;
    }

    // ---------------------- МЕТОДЫ ДОСТУПА (ГЕТТЕРЫ) ----------------------

    /**
     * Геттер (Accessor method): Позволяет безопасно получить значение имени.
     * @return Текущее имя контакта.
     */
    public String getName() {
        return name;
    }

    /**
     * Геттер (Accessor method): Позволяет безопасно получить значение номера телефона.
     * @return Текущий номер телефона контакта.
     */
    public String getPhoneNumber() {
        return phoneNumber;
    }

    // (Сеттеры не добавлены, так как в базовом проекте контакты не редактируются после создания,
    // что сохраняет простоту и целостность данных.)

    // ---------------------- СЕРВИСНЫЕ МЕТОДЫ ----------------------

    /**
     * Переопределение метода toString() из класса Object.
     * * Назначение: Определяет, как объект Contact должен быть представлен в виде строки.
     * Это автоматически вызывается, когда вы печатаете объект (например, System.out.println(contact)).
     * @return Отформатированная строка для вывода в консоль.
     */
    @Override
    public String toString() {
        // String.format используется для создания красивого, выровненного вывода.
        // %-20s - резервирует 20 символов для строки и выравнивает ее по левому краю.
        return String.format("Имя: %-20s | Телефон: %s", name, phoneNumber);
    }

    /**
     * Метод toFileString(): Подготавливает данные контакта к записи в файл.
     * * Назначение: Создает стандартизированный формат данных, который легко
     * десериализовать (прочитать и восстановить) при загрузке.
     * @return Строка данных контакта с разделителем ";".
     */
    public String toFileString() {
        // Разделение полей точкой с запятой (;) - это выбранный нами протокол записи.
        return name + ";" + phoneNumber;
    }
}
