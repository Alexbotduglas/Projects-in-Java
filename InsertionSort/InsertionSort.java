import java.util.Scanner;

// Объявляем класс, в котором будет содержаться основной метод
public class InsertionSort {

    // Основной метод программы
    public static void main(String[] args) {

        // Создаем объект Scanner для чтения входных данных с консоли
        Scanner scanner = new Scanner(System.in);

        // Запрашиваем у пользователя размер массива
        System.out.println("Введите размер массива (N):");

        // Читаем целое число (размер массива)
        int n = scanner.nextInt();

        // Объявляем массив целых чисел с заданным размером N
        int[] arr = new int[n];

        // Запрашиваем у пользователя элементы массива
        System.out.println("Введите " + n + " целых чисел, разделенных пробелами или нажатием Enter:");

        // Цикл для считывания N элементов и заполнения массива
        for (int i = 0; i < n; i++) {
            // Считываем каждый следующий элемент
            arr[i] = scanner.nextInt();
        }

        // Закрываем объект Scanner, так как ввод завершен
        scanner.close();

        // Вызываем метод сортировки вставками
        insertionSort(arr);

        // Выводим сообщение о результате
        System.out.println("\nМассив после сортировки вставками:");

        // Вызываем метод для печати отсортированного массива
        printArray(arr);
    }

    /**
     * Метод, реализующий алгоритм сортировки вставками.
     * @param arr Массив, который нужно отсортировать.
     */
    public static void insertionSort(int[] arr) {

        // Получаем длину массива
        int n = arr.length;

        // Внешний цикл: начинаем с элемента с индексом 1,
        // так как элемент с индексом 0 считается уже отсортированным
        for (int i = 1; i < n; ++i) {

            // Сохраняем текущий элемент, который нужно вставить в отсортированную часть
            int current = arr[i];

            // Инициализируем индекс j для сравнения и сдвига элементов
            // j указывает на последний элемент в уже отсортированной части
            int j = i - 1;

            // Внутренний цикл: перемещаем элементы из отсортированного подмассива,
            // которые больше 'current', на одну позицию вправо
            while (j >= 0 && arr[j] > current) {

                // Сдвигаем элемент arr[j] на позицию j+1
                arr[j + 1] = arr[j];

                // Переходим к предыдущему элементу в отсортированной части
                j = j - 1;
            }

            // Вставляем сохраненный элемент 'current' в правильное место
            // (на позицию j+1, которая освободилась после сдвига)
            arr[j + 1] = current;
        }
    }

    /**
     * Вспомогательный метод для печати элементов массива.
     * @param arr Массив для печати.
     */
    public static void printArray(int[] arr) {
        // Цикл для прохода по всем элементам массива
        for (int i = 0; i < arr.length; i++) {
            // Печатаем текущий элемент
            System.out.print(arr[i] + " ");
        }
        // Переход на новую строку после вывода всех элементов
        System.out.println();
    }
}
